    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String startingpt = (String) jComboBox1.getSelectedItem();
        String destination = (String) jComboBox2.getSelectedItem();
        java.util.Date selectedDate = jDateChooser1.getDate();

        // Convert the selected date to a string in the desired format
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
        String date = sdf.format(selectedDate);

        try {
            Date currentDate = new Date();
            Date userDate = sdf.parse(date);

            if (userDate.before(currentDate)) {
                JOptionPane.showMessageDialog(this, "Error: Please enter a date in the future.", "Date Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Calendar calendar = Calendar.getInstance();
            calendar.add(Calendar.MONTH, 3);
            Date threeMonthsLater = calendar.getTime();

            if (userDate.after(threeMonthsLater)) {
                JOptionPane.showMessageDialog(this, "Error: Please enter a date within the next 3 months.", "Date Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Error parsing date: " + ex.getMessage(), "Date Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        System.out.println("Starting Point: " + startingpt);
        System.out.println("Destination: " + destination);
        System.out.println("Selected Date: " + date);

        // Database connection details
        String url = "jdbc:mysql://localhost:3306/busresv";
        String userName = "root";
        String passWord = "123";

        // Check if the route exists
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            try (Connection con = DriverManager.getConnection(url, userName, passWord);
                 PreparedStatement pst = con.prepareStatement("SELECT * FROM bus WHERE startingpt = ? AND destination = ?")) {
                pst.setString(1, startingpt);
                pst.setString(2, destination);

                try (ResultSet rs = pst.executeQuery()) {
                    // If the ResultSet has any rows, the route exists
                    if (rs.next()) {
                        // Route exists, proceed to the DisplayBus frame
                        DisplayBus2 displayBusFrame = new DisplayBus2(startingpt, destination, date, user.getText(),this);
                        System.out.println("Hi :" + user.getText());
                        displayBusFrame.setVisible(true);
                        displayBusFrame.pack();
                        displayBusFrame.setLocationRelativeTo(null);
                        this.dispose();
                    } else {
                        // Route doesn't exist, show a message
                        JOptionPane.showMessageDialog(this, "Route not available.", "Route Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        } catch (ClassNotFoundException | SQLException ex) {
            // Handle exceptions
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

