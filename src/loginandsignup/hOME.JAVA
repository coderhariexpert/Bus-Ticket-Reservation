    private List<String> getLocations() {
        List<String> locations = new ArrayList<>();

        // Database connection details
        String url = "jdbc:mysql://localhost:3306/busresv";
        String userName = "root";
        String passWord = "123";

        // Fetch distinct starting points from the bus table
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            try (Connection con = DriverManager.getConnection(url, userName, passWord);
                 PreparedStatement pst = con.prepareStatement("SELECT DISTINCT startingpt FROM bus");
                 ResultSet rs = pst.executeQuery()) {

                while (rs.next()) {
                    String startingPoint = rs.getString("startingpt");
                    System.out.println("Starting Point from DB: " + startingPoint);
                    locations.add(startingPoint);
                }
            }
        } catch (ClassNotFoundException | SQLException ex) {
            // Handle exceptions
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
        return locations;
    }

